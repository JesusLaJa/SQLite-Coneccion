    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            //se obtiene la coneccion con el URL
            con = DriverManager.getConnection(url);

            //se valida que la coneccion sea distinto que null
            if (con != null) {

                //se muestra el mensaje en caso de que la coneccion haya sido exitosa
                JOptionPane.showMessageDialog(null, "Coneccion exitosa");
            }

        } catch (Exception e) {
            //se muestra el error en caso de que haya habido algun error
            JOptionPane.showMessageDialog(null, "Error en la coneccion" + e.getMessage().toString());
            System.err.println("Error" + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        modelo.setRowCount(0);
        ResultSet rs = null;

        try {
            PreparedStatement st = con.prepareStatement("select id, nombre from personas");
            rs = st.executeQuery();

            while (rs.next()) {
                modelo.addRow(new Object[]{rs.getInt("id"), rs.getString("nombre")});

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error" + e.getMessage());
            System.err.println("Error " + e.getMessage());

        }
    }//GEN-LAST:event_jButton2ActionPerformed

